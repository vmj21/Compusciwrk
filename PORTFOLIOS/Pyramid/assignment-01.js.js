
function cb(click) {
  const cbS = document.getElementById('cbS');
  const sumvalue = parseInt(cbS.innerText) + click;

  cbS.innerText = sumvalue;
 
  if (sumvalue < 0) {
      cbS.innerText = 0;
  }
}

